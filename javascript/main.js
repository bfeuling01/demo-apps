function add(a,b) {
    return a + b;
}

function subtract(a,b) {
    return a - b;
}

function multiply(a,b) {
    return a * b;
}

function divide(a,b) {
    return a / b;
}

function modulo(a,b) {
    return a % b;
}

exports.modulo = modulo;
exports.add = add;
exports.subtract = subtract;
exports.multiply = multiply;
exports.divide = divide;
